/**
 *
 * modifier/belirtec
 *
 * access modifier (public, private, protected)
 * non-access modifier (final, abstract, static)
 *
 * class ve interface'ler için access modifier olarak sadece public kullanılabilir.
 * eğer public keyword'ü yoksa, default tur. (access level)
 * public ise herkes erişebilir, default/friendly/package level -> sadece aynı paketten erişilebilir.
 * final keyword'ü class, method ve variable için kullanılabilir.
 * final keyword'ü, class için kullanılırsa eğer kalıtım yapılamaz (extends). String, Integer
 * final ile abstract birbirine zıttır. Bunlar bir araya gelemez.
 * abstract keyword'ü bir class için kullanılıyorsa kalıtılması maksadıyla kullanılmaktadır.
 *
 *
 */
